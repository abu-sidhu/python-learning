#relational operators
first_num=14
sec_num=20
print(first_num<sec_num)
print(first_num>sec_num)
print(first_num<=sec_num)
print(first_num>=sec_num)
print(first_num==sec_num)
print(first_num!=sec_num)


#logical
print(first_num==14 and sec_num>10)
print(first_num==14 and sec_num<10)
print(first_num==14 or sec_num<10)
print(not sec_num<10)